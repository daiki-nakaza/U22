void MapDisp() {			// マップの描画
	//背景スクロール描画
	if (Stage == 1) {
		DrawGraph(0 - Map_PlayerX % 1024, 0 - Map_PlayerY, g_BackGroundImage1, FALSE);
		DrawGraph(1024 - Map_PlayerX % 1024, 0 - Map_PlayerY, g_BackGroundImage1, FALSE);
	}
	else if (Stage == 2) {
		DrawGraph(0 - Map_PlayerX % 1024, 0 - Map_PlayerY, g_BackGroundImage2, FALSE);
		DrawGraph(1024 - Map_PlayerX % 1024, 0 - Map_PlayerY, g_BackGroundImage2, FALSE);
	}
	else if (Stage == 3) {
		DrawGraph(0 - Map_PlayerX % 1024, 0 - Map_PlayerY, g_BackGroundImage3, FALSE);
		DrawGraph(1024 - Map_PlayerX % 1024, 0 - Map_PlayerY, g_BackGroundImage3, FALSE);
	}
	
	for (int y = 0; y < HEIGHT + 1; y++) {
		for (int x = 0; x < WIDTH + 1; x++) {

			// 画面からはみ出た位置なら描画しない
			if (x + MapX < 0 || y + MapY < 0 || x + MapX >= WIDTH * MAP_LONG) continue;

			//
			MapChipNumX = 0;
			MapChipNumY = 0;
			while (x + MapX + (MapChipNumX) >= WIDTH) {
				MapChipNumX -= WIDTH;
				MapChipNumY += HEIGHT;
			}

			switch (g_MapChip[y + MapY + MapChipNumY][x + MapX + MapChipNumX])
			{
			case 0:
				/*DrawBox(x * MAP_SIZE + MapDrawPointX, y * MAP_SIZE + MapDrawPointY,
					(x + 1) * MAP_SIZE + MapDrawPointX, (y + 1) * MAP_SIZE + MapDrawPointY,
					GetColor(255, 255, 0), TRUE);*/
				DrawGraph(x * MAP_SIZE + MapDrawPointX, y * MAP_SIZE + MapDrawPointY, MapTip, TRUE);
				break;
			case 2:
				if (ClearTime == 0) {
					DrawGraph(x * MAP_SIZE + MapDrawPointX, y * MAP_SIZE + MapDrawPointY, g_Door[0], TRUE);
				}
				else if (ClearTime != 0) {
					DrawGraph(x * MAP_SIZE + MapDrawPointX, y * MAP_SIZE + MapDrawPointY, g_Door[(ClearTime + 30)/30], TRUE);
				}
				break;
			case 3:
				DrawBox(x * MAP_SIZE + MapDrawPointX, y * MAP_SIZE + MapDrawPointY,
					(x + 1) * MAP_SIZE + MapDrawPointX, (y + 1) * MAP_SIZE + MapDrawPointY,
					GetColor(0, 255, 0), TRUE);
				break;
			case 4:
				DrawBox(x * MAP_SIZE + MapDrawPointX, y * MAP_SIZE + MapDrawPointY,
					(x + 1) * MAP_SIZE + MapDrawPointX, (y + 1) * MAP_SIZE + MapDrawPointY,
					GetColor(0, 255, 0), TRUE);
				break;
			case 5:
				DrawBox(x * MAP_SIZE + MapDrawPointX, y * MAP_SIZE + MapDrawPointY,
					(x + 1) * MAP_SIZE + MapDrawPointX, (y + 1) * MAP_SIZE + MapDrawPointY,
					GetColor(0, 255, 0), TRUE);
				break;
			case 10:
				break;
			}
			SetFontSize(10);
			//DrawFormatString(x * MAP_SIZE + MapDrawPointX, y * MAP_SIZE + MapDrawPointY, 0xffffff, "%d %d",  y + MapY + MapChipNumY,x + MapX + MapChipNumX );
		}
	}
}


void MapDisp() {			// マップの描画
	for (int y = 0; y < HEIGHT; y++) {
		for (int x = 0; x < WIDTH + 1; x++) {

			// 画面からはみ出た位置なら描画しない
			if (x + MapX < 0 || y + MapY < 0 || x + MapX >= WIDTH * 2) continue;

			//
			MapChipNumX = 0;
			MapChipNumY = 0;
			while (x + MapX + (MapChipNumX) >= WIDTH) {
				MapChipNumX -= WIDTH;
				MapChipNumY += HEIGHT;
			}

			switch (g_MapChip[y + MapY + MapChipNumY][x + MapX + MapChipNumX])
			{
			case 0:
				DrawBox(x * MAP_SIZE + MapDrawPointX, y * MAP_SIZE + MapDrawPointY,
					(x + 1) * MAP_SIZE + MapDrawPointX, (y + 1) * MAP_SIZE + MapDrawPointY,
					GetColor(255, 255, 0), TRUE);
				break;
			case 1:
				DrawBox(x * MAP_SIZE + MapDrawPointX, y * MAP_SIZE + MapDrawPointY,
					(x + 1) * MAP_SIZE + MapDrawPointX, (y + 1) * MAP_SIZE + MapDrawPointY,
					GetColor(0, 255, 0), TRUE);
				break;
			case 7:DrawBox(x * MAP_SIZE + MapDrawPointX, y * MAP_SIZE + MapDrawPointY,
				(x + 1) * MAP_SIZE + MapDrawPointX, (y + 1) * MAP_SIZE + MapDrawPointY,
				GetColor(255, 255, 255), TRUE);
				break;
			case 5:
				DrawBox(x * MAP_SIZE + MapDrawPointX, y * MAP_SIZE + MapDrawPointY,
				(x + 1) * MAP_SIZE + MapDrawPointX, (y + 1) * MAP_SIZE + MapDrawPointY,
				GetColor(0, 255, 0), TRUE);
				DrawCircle(x * MAP_SIZE + MapDrawPointX + 16, y * MAP_SIZE + MapDrawPointY + 26,
					4, 0x000000, true);
				break;
			case 8:
				DrawBox(x * MAP_SIZE + MapDrawPointX, y * MAP_SIZE + MapDrawPointY,
					(x + 1) * MAP_SIZE + MapDrawPointX, (y + 1) * MAP_SIZE + MapDrawPointY,
					GetColor(0, 255, 0), TRUE);
				DrawCircle(x * MAP_SIZE + MapDrawPointX + 16, y * MAP_SIZE + MapDrawPointY + 16,
					16, 0x000000, true);
				break;
			}
			DrawLine(x * MAP_SIZE + MapDrawPointX, y * MAP_SIZE, x * MAP_SIZE + MapDrawPointX, y * MAP_SIZE + MAP_SIZE, 0x0000ff);
			DrawLine(x * MAP_SIZE + MapDrawPointX, y * MAP_SIZE, x * MAP_SIZE + MAP_SIZE + MapDrawPointX, y * MAP_SIZE, 0x0000ff);
		}
	}
}

